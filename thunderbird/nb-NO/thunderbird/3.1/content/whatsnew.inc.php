<h2>Hva er nytt i Thunderbird 3.1</h2>

<p>Vi håper at du liker de nye funksjonene i Thunderbird. Vi har nye søkeverktøy, e-postfaner, meldingsarkivering og hundrevis av utvidelser som gjør Thunderbird 3.1 til et verkøy laget for deg.</p>

<h2>Enklere å oppgradere med ny migreringsassistent</h2>

<p>På Hjelp-menyen, velg Migreringsassistent for å få hjelp til å endre verktøylinjeutseendet, og å legge til funksjoner som sammenslått meldingshoder, eller avanserte mappekolonner.</p>

<h2>Bedre søk med nye søkeverktøy</h2>

<p>Den nye filter-verktøylinjen lar deg raskere få oversikt over e-posten din. Filtrer e-posten din etter nye meldinger, etiketter, personer i adresseboken, eller med et søkeord. Du kan også &quot;låse&quot; eller lagre et filter og bruke det på tvers av flere mapper. </p>

<p>Skriv inn søkeordet ditt i den globale søkelinjen, og vi åpner søkeresultatet i en ny fane. Du kan stille inn nøyaktig hva du leter etter med Thunderbirds filter- og tidslinjeverktøy. </p>

<h2>E-postfaner</h2>

<p>Dersom du liker fanearkene i Firefox kommer du til å elske e-postfaner. Dobbeltklikk eller trykk på Enter på en e-postmelding for å åpne den i en ny fane. Du kan høyreklikke på meldinger eller mapper for å åpne dem i en fane i bakgrunnen.</p>

<h2>Meldingsarkivering</h2>

<p>Ved å trykke på Arkiver-knappen, eller ‘A’-tasten, vil e-post arkiveres. Arkivering av e-post flytter den fra innboksen til et nytt arkivmappesystem.</p>

<h2>Flere nye funksjoner å utforske:</h2>

<ul>
  <li>Ett-trykks adressebok</li>
  <li>Vedleggspåminnelse</li>
  <li>Sammenslåtte mapper</li>
  <li>Aktivitetsbehandling</li>
  <li>Hundrevis av nye utvidelser å prøve ut</li>
</ul>

