<p>Me loodame, et naudid uusi Thunderbirdi võimalusi. Meil on uued otsinguvahendid, kaartidel e-post, sõnumite arhiveerimine ja sajad lisad, millega saad Thunderbird 3.1 päris enda omaks muuta. </p>

