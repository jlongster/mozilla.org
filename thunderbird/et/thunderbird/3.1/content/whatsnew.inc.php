<p>Sisesta otsisõna üldotsingusse ja me avame leitud vasted uuel kaardil. Thunderbirdi filtreerimis- ja ajajoone tööriistade abil saad otsitava asukoha täpselt määrata.</p>

<h2>Kaartidel e-post</h2>

<p>Kui sulle meeldib Firefoxi kaartidega sirvimine, siis meeldib sulle kindlasti kaartidel e-post. Tee topeltklõps sõnumil või vajuta enter selle uuel kaardil avamiseks. Paremklõpsuga sõnumil või kaustal saad selle avada taustakaardil.</p>

