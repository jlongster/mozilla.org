<h2>Kva er nytt i Thunderbird 3.1</h2>

<p>Vi håper at du likar dei nye funksjonane i Thunderbird. Vi har nye søkjeverktøy, e-postfaner, meldingsarkivering og hundrevis av utvidingar som gjer Thunderbird 3.1 til eit verkøy laga for deg.</p>

<h2>Enklare å oppgradere med ny migrasjonsassistent</h2>

<p>På Hjelp-menyen, vel Migrasjonsassistent for å få hjelp til å endre verktøylinjeutsjånaden, og å leggje til funksjonar som samanslåtte meldingshovud, eller avanserte mappekolonner.</p>

<h2>Betre søk med nye søkjeverktøy</h2>

<p>Den nye filter-verktøylinja lèt deg raskare få oversikt over e-posten din. Filtrer e-posten din etter nye meldingar, merkelappar, personar i adresseboka, eller med eit søkjeord. Du kan òg &quot;låse&quot; eller lagre eit filter og bruke det på tvers av fleire mapper. </p>

<p>Skriv inn søkjeordet ditt i den globale søkjelinja, og vi opner søkjeresultatet i ei ny fane. Du kan stille inn nøyaktig det du leitar etter med Thunderbird sitt filter- og tidslinjeverktøy. </p>

<h2>E-postfaner</h2>

<p>Dersom du likar fanearkene i Firefox kjem du til å elske e-postfaner. Dobbelklikk eller trykk på Enter på ei e-postmelding for å opne henne i ei ny fane. Du kan høgreklikke på meldingar eller mapper for å opne dei i ei fane i bakgrunnen.</p>

<h2>Meldingsarkivering</h2>

<p>Ved å trykke på Arkiv-knappen, eller ‘A’-tasten, vert e-post arkivert. Arkivering av e-post flyttar den frå innboksen til eit nytt arkivmappesystem.</p>

<h2>Fleire nye funksjonar å utforske:</h2>

<ul>
  <li>Eitt-trykks adressebok</li>
  <li>Vedleggspåminning</li>
  <li>Samanslåtte mapper</li>
  <li>Aktivitetshandsaming</li>
  <li>Hundrevis av nye utvidingar å prøve ut</li>
</ul>

