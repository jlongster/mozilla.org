<h2>Wat is er nieuw in Thunderbird 3.1</h2>

<p>We hopen dat de nieuwe functies in Thunderbird u zullen bevallen. We hebben nieuwe zoekhulpmiddelen, e-mail in tabbladen, e-mailarchivering en honderden add-ons om Thunderbird 3.1 volledig naar uw smaak te personaliseren. </p>

<h2>Makkelijker upgraden met de nieuwe Migratieassistent</h2>

<p>Selecteer de Migratieassistent in het menu Help om de lay-out van uw werkbalk te helpen wijzigen en functies als Compact Header en Advanced Folder Columns toe te voegen. </p>

<h2>Beter zoeken met nieuwe zoekhulpmiddelen</h2>

<p>Met de nieuwe Snelfilterwerkbalk kunt u uw e-mail sneller filteren. Filter uw e-mail op nieuwe berichten, labels, personen in uw adresboek, of door het gebruik van zoektermen. U kunt een filter ook “vastzetten” of opslaan en in meerdere mappen gebruiken. </p>

<p>Voer uw zoekterm in de algemene zoekbalk in en uw zoekresultaten worden in een nieuw tabblad geopend. Met Thunderbirds filter- en tijdlijnhulpmiddelen kunt u precies dat e-mailbericht vinden dat u zoekt. </p>

<h2>E-mail in tabbladen</h2>

<p>Als navigeren met tabbladen in Firefox u bevalt, zult u e-mail in tabbladen ook geweldig vinden. Door op een e-mailbericht te dubbelklikken of op Enter te drukken opent u het bericht in een nieuw tabblad. Met de rechtermuisknop opent u berichten en mappen in een tabblad op de achtergrond.</p>

<h2>E-mailarchivering</h2>

<p>U kunt uw e-mail archiveren door op de knop Archiveren te klikken of op de toets ‘A’ te drukken. Het archiveren van e-mail verplaatst deze van uw Postvak IN naar het nieuwe archiefmappensysteem.</p>

<h2>Meer nieuwe functies om te ontdekken:</h2>

<ul>
  <li>Eén-klik-adresboek</li>
  <li>Bijlageherinnering</li>
  <li>Samengevoegde mappen</li>
  <li>Activiteitenbeheerder</li>
  <li>Honderden nieuwe add-ons om te proberen</li>
</ul>

