<h2>Hvað er nýtt í Thunderbird 3.1</h2>

<p>Við vonum að þú hafir gagn af nýju hlutunum í Thunderbird. Við erum með nýja leit, póst í flipum, safnvistun á pósti, og hundruð viðbóta sem gera Thunderbird 3.1 að þínu eigin forriti. </p>

<h2>Auðveldara að uppfæra með flutningshjálpinni</h2>

<p>Í hjálparvalmyndinni, veldu Flutningshjálp til að hjálpa þér að breyta útliti verkfærasláa og bæta við nýjum eiginleikum eins og samanþjöppuð sýn og ítarlegir dálkar. </p>

<h2>Betri leit með nýjum leitarverkfærum</h2>

<p>Nýja flýtisíusláin gerir þér kleift að sía póstinn á fljótari hátt. Þú getur síað póstinn eftir nýjum	pósti, flokkum, og fólki í nafnaskrá, eða notað leitarorð. Þú getur einnig &quot;fest&quot; eða vistað síu og notað hana á margar möppur. </p>

<p>Sláðu inn leitartexta í víðværu leitarstikuna og þá opnast leitarniðurstöður í nýjum flipa. Þú getur fundið nákvæmlega það sem þú ert að leita að með því að nota Thunderbird síur og tímalínutól. </p>

<h2>Flipar</h2>

<p>Ef þú ert ánægður með flipa í Firefox, muntu dýrka tölvupóst í flipum. Tvísmelltu eða ýttu á Enter í tölvupósti til að opna póstinn í nýjum flipa. Þú getur hægri smellt á póst eða möppur til að opna í flipa í bakgrunni.</p>

<h2>Safnvistun</h2>

