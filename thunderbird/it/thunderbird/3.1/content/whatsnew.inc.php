<h2>Le novità di Thunderbird 3.1</h2>

<p>Ci auguriamo che tu possa apprezzare le nuove caratteristiche introdotte in Thunderbird. Abbiamo aggiunto nuovi strumenti di ricerca, la possibilità di archiviare i messaggi, la visualizzazione a schede della posta, senza dimenticare centinaia di componenti aggiuntivi per rendere Thunderbird 3.1 veramente tuo.</p>

<h2>Aggiornamento più semplice grazie alla nuova Migrazione guidata</h2>

<p>All'interno del menu Aiuto seleziona Migrazione guidata: questa procedura ti aiuterà a modificare la struttura delle barre degli strumenti e ti guiderà nell'aggiunta di nuove caratteristiche, come ad esempio Compact Header e Advanced Folder Columns.</p>

<h2>Ricerche più efficaci grazie ai nuovi strumenti di ricerca</h2>

<p>La nuova barra degli strumenti Filtro rapido ti consente di filtrare le e-mail più velocemente. Puoi scegliere di visualizzare solo i nuovi messaggi, i messaggi con etichette, le mail inviate da contatti presenti nella tua rubrica oppure filtrare in base a dei termini di ricerca. Puoi anche &quot;fissare&quot; o salvare un filtro e utilizzarlo in più cartelle.</p>

<p>Inserisci i termini da cercare nella barra di ricerca globale e visualizza i risultati in una nuova scheda. Potrai utilizzare i nuovi filtri e il grafico della sequenza temporale (timeline) per individuare i messaggi che stavi cercando.</p>

<h2>Visualizzazione a schede dei messaggi</h2>

<p>Se ti trovi a tuo agio con la navigazione a schede di Firefox, certamente imparerai ad apprezzare la visualizzazione a schede della posta. Fai doppio clic o premi invio su un messaggio per aprirlo in una nuova scheda. Puoi anche utilizzare il tasto destro del mouse per aprire i messaggi e le cartelle in una scheda in secondo piano.</p>

<h2>Archiviazione dei messaggi</h2>

<p>Puoi archiviare la tua posta usando il pulsante Archivia o premendo il tasto `A'. In questo modo il messaggio verrà spostato dalla posta in arrivo alla cartella Archivi, parte del nuovo sistema di organizzazione della posta.</p>

<h2>Altre novità da esplorare:</h2>

<ul>
  <li>Rubrica a portata di clic</li>
  <li>Promemoria allegati dimenticati</li>
  <li>Cartelle unificate</li>
  <li>Registro attività</li>
  <li>Centinaia di estensioni da provare</li>
</ul>

