<h2>Thunderbird 3.1'deki Yenilikler</h2>

<p>Thunderbird'deki yeni özelliklerden hoşlanacağınızı umuyoruz. Thunderbird 3.1'i her şeyiyle sizin yapmak için yeni arama araçları, sekmeli e-posta, ileti arşivleme ve yüzlerde eklentimiz var. </p>

<h2>Yeni Geçiş Yardımcısı ile Yükseltmek Daha Kolay</h2>

<p>Yardım menüsünden ulaşabileceğiniz Geçiş Yardımcısı, araç çubuğu düzeninizi değiştirmenize ve Kısa Başlık ile Gelişmiş Dizin Sütunları gibi özellikleri eklemenize yardımcı olur. </p>

<h2>Yeni Arama Araçlarıyla Daha İyi Arama</h2>

<p>Yeni Hızlı Süzgeç Arama Çubuğu e-postalarınızı daha hızlı süzmenizi sağlar. E-postalarınızı yeni iletilere, etiketlere, adres defterinizdeki kişilere ya da arama terimlerine göre süzün. Ayrıca bir süzgeci &quot;sabitleyerek&quot; ya da kaydederek birden çok dizinde kullanabilirsiniz. </p>

<p>Aranacak terimleri Global Arama Çubuğu'na yazarsanız arama sonuçlarınız yeni bir sekmede açılır. Thunderbird'ün süzme ve zaman çizelgesi araçlarıyla, aradığınızı kolayca bulabilirsiniz. </p>

<h2>Sekmeli E-posta</h2>

<p>Firefox'un sekmeli taramasını beğeniyorsanız sekmeli e-postayı da seveceksiniz. İletileri yeni bir sekmede açmak için iletiye çift tıklayın ya da enter'a basın. İletilere veya dizinlere sağ tıklayarak arka planda bir sekmede açılmalarını sağlayabilirsiniz.</p>

<h2>İleti Arşivleme</h2>

<p>Arşivle düğmesine tıklayarak ya da ‘A’ tuşuna basarak e-postanızı aşivleyebilirsiniz. Arşivlediğiniz e-postalar, gelen kutunuzdan yeni arşiv dizini sistemine taşınır.</p>

<h2>Keşfedebilmeyi bekleyen diğer yeni özellikler:</h2>

