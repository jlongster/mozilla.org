<h2>Kaj je novega v Thunderbirdu 3.1</h2>

<p>Upamo, da boste uživali v novih funkcijah Thunderbirda. Imamo nova orodja za iskanje, pošto po zavihkih, arhiviranje, in na stotine razširitev, s katerimi si lahko Thunderbird 3.1 po svoje uredite. </p>

<h2>Z novim pomočnikom za selitev je laže posodobiti </h2>

<p>V meniju za pomoč izberite pomočnika za selitev, ki vam bo pomagal prirediti vašo  orodno vrstico in dodati funkcije, kot sta  povzetek glave in  dodatni stolpci za mape.  </p>

<h2>Boljše iskanje s pomočjo novih orodij</h2>

<p>Z novo orodno vrstico za hitri filter lahko po svoji pošti brskate precej hitreje. Iščite po svoji pošti za nova sporočila, oznake, osebe iz vašega adresarja, ali z uporabo izrazov za iskanje. Filter lahko tudi "pripnete",  shranite ali pa uporabite za več map naenkrat. </p>

<p>Vnesite iskalni izraz v Globalno iskalno vrstico in program vam bo rezultate iskanja odprl v novem zavihku. S pomočjo pripomočkov za filtriranje in iskanje boste v novem Thunderbirdu hitro našli, kar iščete. </p>

<h2>Zavihki za pošto</h2>

<p>Če  Firefoxu radi brskate s pomočjo zavihkov, Vam bodo novi zavihki gotovo všeč. Dvokliknite na sporočilo ali pa vtipkajte Enter, in sporočila se bodo odprla v novem zavihku. Kesnokliknete lahko na pošto ali mape, in odprle se  bodo v zavihku v ozadju.</p>

<h2>Arhiviranje sporočil</h2>

<p>S klikom na gumb Arhiv ali s tipko "A" lahko arhivirate svojo pošto.  Z arhiviranjem prestavite svojo pošto iz predala za dospela sporočila v nov sistem map za arhiviranje. </p>

