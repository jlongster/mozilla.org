<h2>Ce este nou în Thunderbird 3.1</h2>

<p>Sperăm să vă bucurați de noile funcționalități din Thunderbird. Avem noi instrumente de căutare, email cu file, arhivare mesaje și sute de suplimente pentru a face din Thunderbird 3.1 cu totul pe placul dumneavoastră. </p>

<h2>Mai ușor de actualizat cu noul Asistent de migrare</h2>

<p>În meniul Ajutor, selectați Asistent de migrare pentru a vă ajuta să vă schimbați aspectul barei de instrumente și să adăugați funcționalități cum ar fi Antete pliate și Coloane dosar avansate. </p>

<h2>Căutare mai bună cu noile unelte pentru căutare</h2>

<p>Noua bară de instrumente Filtru rapid vă permite să filtrați emailurile dumneavoastră mai repede. Filtrați-vă emailurile prin mesaje noi, etichete, oameni din agenda dumneavoastră sau folosind termeni de căutare. De asemenea, puteți să  &quot;Fixați&quot; sau să salvați un filtru și să-l utilizați în mai multe dosare. </p>

<p>Scrieți termenul căutat de dumneavoastră în bara de căutare globală și vă vom deschide rezultatele căutării într-o filă nouă. Aveți posibilitatea să fixați repere la ceea ce căutați cu instrumentele filtrare și cronologie din Thunderbird.</p>

<h2>File email</h2>

<p>Dacă vă place navigarea cu file din Firefox, ne-am gândit că le veți îndrăgi și în email. Clic dublu sau apăsați enter pe un mesaj pentru a deschide mesajele într-o filă nouă. Aveți posibilitatea să faceți clic dreapta pe mesaje sau dosare pentru a le deschide într-o filă în fundal.</p>

<h2>Arhivarea mesajelor</h2>

<p>Apăsând butonul Arhivează sau tasta „A” puteți arhiva un mesaj.  Arhivarea mută mesajele din dosarul cu mesaje primite în dosarul Arhivate.</p>

