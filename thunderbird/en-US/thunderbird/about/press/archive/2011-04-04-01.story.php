<p>April 4, 2011</p>

<p>Editor's Note: On April 4, Mozilla Messaging will be integrating with Mozilla Labs to consolidate our efforts to drive the future of messaging on the web.  David Ascher will head up a new innovation group within Mozilla Labs focused on online communications and social interactions for Web users.</p>

<p>For more details, check out Mitchell Baker's <a href="http://blog.lizardwrangler.com/?p=2734">blog post</a> (reposted below) and David Ascher's <a href="http://ascher.ca/blog/2011/04/04/the-future-of-messaging/ ">blog post</a> on his new role.</p>

<p>"The Web has changed a lot in the last few years.  One of the big changes is how much we now use the Web for messaging, communication and social interactions.  We post messages on social networking sites, we tweet, we get messages (often known as "notifications") from applications, we use Web-based mail systems.  The pace and importance of innovation in this space is enormous and growing.</p>
<p>Mozilla has been exploring new ways to put people in control of their online communications and social interactions for a couple of years now.  We currently have two teams.  One is the team at Mozilla Messaging, which produces Thunderbird and messaging innovations such as Raindrop and F1.  The second team is within Mozilla Labs, and has been working on identity, contacts and related topics.</p>
<p>We intend to combine the two teams to increase our effectiveness.  Practically this means we'll be integrating Mozilla Messaging with Mozilla Labs.   David Ascher will lead a new innovation group within Mozilla Labs focused on online communications and social interactions on the Web.  After the teams merge into Mozilla Labs we will dissolve Mozilla Messaging.  This simplifies our overall structure.  I'd like to offer deep thanks to Marten Mickos and Chris Beard for serving on the Mozilla Messaging Board of Directors, along with David Ascher. </p>
<p>The Thunderbird team will continue to develop and release Thunderbird from its new home within the combined organization.   David Ascher will  continue to oversee our Thunderbird product.  Thunderbird users and contributors should see no difference in their experience.  Email is a solid and foundational technology which retains immense value.  The Thunderbird team has been a great success in re-making Thunderbird into a modern email client.  Thunderbird now has a more modular architecture, vastly modernized codebase, effective and growing add-on mechanisms, a vastly improved user interface, and incremental innovations that continue to evolve and move the product forward. We intend to continue our work with the Thunderbird email product to meet this need.</p>
<p>The innovations occurring today in online communications and social interaction are astonishing. It's a wildly vibrant time.</p>
<p>Please join us as we put more concentrated focus on our efforts in these areas."</p>
