<h2>O que há de novo no Thunderbird 3.1</h2>

<p>Esperamos que desfrute das novas funcionalidades do Thunderbird. Temos novas ferramentas de pesquisa, correio por separadores, arquivamento de mensagens e centenas de extras para tornar o Thunderbird 3.1 só seu. </p>

<h2>Fácil de actualizar com o novo assistente de migração</h2>

<p>No menu Ajuda, seleccione Assistente de migração para o ajudar a alterar o esquema das suas barras de ferramentas e as funcionalidades como Compactar cabeçalho e Colunas das pastas avançadas. </p>

<h2>Melhor pesquisa com as novas ferramentas de pesquisa</h2>

<p>A nova Barra de Ferramentas de Filtro rápido permite-lhe filtrar o seu correio mais rapidamente. Filtre o seu correio por Novas mensagens, Etiquetas e pessoas no seu Livro de endereços, ou use termos de pesquisa. Pode ainda fazer "Pin" ou guardar o filtro para o utilizar em outras pastas. </p>

<p>Escreva o seu termo de pesquisa na Barra de Pesquisa Global e abrimos os resultados da pesquisa num novo separador. Pode indicar o que procura com as ferramentas de filtragem e de tempo do Thunderbird. </p>

<h2>Correio com separadores</h2>

<p>Se gostou da navegação por separadores do Firefox vai adorar o correio com separadores. Faça duplo clique ou carregue em Enter numa mensagem de correio e abre a mensagem num novo separador. Um clique direito em mensagens ou pastas abre-as num novo separador em pano de fundo.</p>

<h2>Arquivamento de mensagens</h2>

<p>Clicar no botão Arquivar o a tecla ‘A’ key arquiva o seu correio. Ao arquivar o correio o correio é movido da sua caixa de entrada para a nova pasta de arquivamento do sistema.</p>

<h2>Outras funcionalidades novas a explorar:</h2>

<ul>
  <li>Livro de endereços com um clique</li>
  <li>Lembrança de anexos</li>
  <li>Pastas unificadas</li>
  <li>Gestor de actividade</li>
  <li>Centenas de novos extras para experimentar</li>
</ul>

