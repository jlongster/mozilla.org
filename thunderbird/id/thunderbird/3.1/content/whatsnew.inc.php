<h2>Yang Baru di Thunderbird 3.1</h2>

<p>Kami berharap Anda menikmati fitur baru di Thunderbird. Perangkat pencarian yang baru, tab untuk email, pengarsipan pesan, dan beratus pengaya agar Thunderbird 3.1 sesuai dengan selera Anda. </p>

<h2>Lebih Mudah dalam Pembaruan dengan Asisten Migrasi</h2>

<p>Dari menu Bantuan, pilih Asisten Migrasi untuk membantu Anda mengubah tata letak Bilah Alat dan menambahkan fitur baru seperti Tajuk Kompak dan Kolom Folder Canggih. </p>

<h2>Pencarian yang Lebih Baik dengan Perangkat Pencarian yang Baru</h2>

<p>Bilah Alat Filter Cepat dapat Anda gunakan untuk memfilter email dengan cepat. Saring email Anda untuk Pesan Baru, Tag, kenalan di Buku Alamat, atau menggunakan istilah pencarian. Anda juga dapat menempelkan atau menyimpan sebuah filter yang dapat Anda gunakan di folder lainnya. </p>

<p>Ketikkan kata yang Anda cari pada Bilah Pencarian Global dan kami akan menampilkan hasilnya pada tab baru. Anda dapat menelusuri apa yang Anda cari dengan sistem filter Thunderbird serta perangkat garis waktu. </p>

<h2>Email dengan Tab</h2>

<p>Jika Anda menyukai fitur penjelajahan tab pada Firefox, Anda akan menyukai penggunaan tab untuk menampilkan email. Klik ganda atau tekan enter pada sebuah pesan email dan sebuah tab akan terbuka. Anda dapat mengklik kanan pada pesan atau folder untuk membukanya pada tab di belakang tab yang tampil sehingga tidak menganggu tampilan.</p>

<h2>Pengarispan Pesan</h2>

<p>Klik tombol Arsip atau pencet tombol 'A' agar email Anda terarsipkan. Mengarsipkan email memindahkan email dari kotak masuk ke sistem folder arsip yang baru.</p>

<h2>Fitur Lainnya untuk Dijelajahi:</h2>

<ul>
