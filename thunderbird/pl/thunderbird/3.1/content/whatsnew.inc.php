<h2>Co nowego w Thunderbirdzie 3.1</h2>

<p>Mamy nadzieję, że nowe możliwości programu Mozilla Thunderbird przypadną Wam do gustu - dodaliśmy nowe narzędzia wyszukiwania, obsługę kart, archiwizowanie wiadomości i dziesiątki rozszerzeń, aby Thunderbird jeszcze lepiej spełniał Wasze oczekiwania.</p>

<h2>Prostsza aktualizacja z nowym asystentem migracji</h2>

<p>Z menu Pomoc wystarczy wybrać Asystent migracj, aby dostosować nowe możliwości jak elementy paska narzędzi czy nagłówka wiadomości lub kolumn folderów.</p>

<h2>Lepsze wyszukiwanie z nowymi narzędziami</h2>

<p>Nowy pasek filtrowania pozwala szybciej wyszukiwać wiadomości względem statusu przeczytania, etykiet, kontaktów z książki adresowej lub innych warunków. Możliwe jest także &quot;przypięcie&quot; lub zachowanie filtru, w celu wykorzystania w innych folderach.</p>

<p>Aby otworzyć wyniki wyszukiwania w nowej karcie wystarczy wprowadzić szukaną frazę w głównym pasku wyszukiwania. Interfejs wyszukiwania zawiera narzędzia filtrowania i zawężania przeszukiwanego okresu, aby łatwiej odszukać wiadomości.</p>

<h2>Obsługa kart</h2>

<p>Jeżeli używasz kart w swojej przeglądarce internetowej, karty w Thunderbirdzie powinny również przypaść Ci do gustu. Podwójne kliknięcie lewym przyciskiem myszki lub enter na klawiaturze otworzy zaznaczoną wiadomość w nowej karcie. Klikając środkowym przyciskiem myszki lub poprzez menu kontekstowe można również otwierać wiadomości i foldery w tle.</p>

<h2>Archiwizowanie wiadomości</h2>

<p>Wybierając przycisk "Archiwizuj" lub klawisz "A" można przenieść pojedyncze wiadomości jak i całe wątki do folderu archiwum.</p>

<h2>Inne nowości:</h2>

<ul>
  <li>Odświeżona książka adresowa</li>
  <li>Przypomnienia o brakujących załącznikach</li>
  <li>Pogrupowane foldery</li>
  <li>Menedżer aktywności</li>
  <li>Nowy menedżer dodatków i setki nowych rozszerzeń</li>
</ul>

<p>Dodatkowe informacje o Thunderbirdzie można znaleźć na <a href="/thunderbird/">stronie domowej programu</a>.</p>

