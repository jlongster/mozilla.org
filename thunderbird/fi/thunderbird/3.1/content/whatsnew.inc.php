<h2>Thunderbird 3.1:n uudet ominaisuudet</h2>

<p>Toivomme, että pidät Thunderbirdin uusista ominaisuuksista. Thunderbird 3.1 toimii juuri niin kuin haluat uusien hakutyökalujen, sähköpostivälilehtien, viestiarkiston ja satojen laajennusten ansiosta.</p>

<h2>Uusi Siirtymisavut-toiminto helpottaa päivityksiä</h2>

<p>Valitse Ohje-valikosta Siirtymisavut, ja voit muuttaa työkalupalkin asettelua ja lisätä ominaisuuksia kuten supistetut otsakenäkymät ja kansioiden lisäsarakkeet.</p>

<h2>Paremmat hakutyökalut</h2>

<p>Pikasuodatuspalkin avulla suodatat viestisi nopeammin tilan, avainsanojen, osoitekirjan yhteystietojen tai hakusanojen mukaan. Voit myös säilyttää suodatusasetukset, kun vaihdat kansiota. </p>

<p>Kirjoita hakusana hakupalkkiin, ja hakutulokset avataan uuteen välilehteen. Thunderbirdin suodatus- ja aikajanatyökaluilla löydät juuri sen, mitä etsitkin.</p>

<h2>Sähköpostivälilehdet</h2>

<p>Jos pidät Firefoxin välilehdistä, ihastut varmasti välilehtien käyttöön myös sähköpostissa. Kaksoisnapsautus tai Enterin painallus avaa viestin uuteen välilehteen. Voit avata viestejä taustavälilehtiin hiiren oikean painikkeen kautta.</p>

<h2>Viestiarkisto</h2>

<p>Voit arkistoida viestisi valitsemalla Arkistoi-painikkeen tai painamalla A. Arkistointi siirtää viestit postilaatikostasi uuteen arkistokansiojärjestelmään.</p>

<h2>Muita tutustumisen arvoisia ominaisuuksia:</h2>

