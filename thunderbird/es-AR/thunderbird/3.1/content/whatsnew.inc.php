<h2>Qué hay de nuevo en Thunderbird 3.1</h2>

<p>Deseamos que disfrutes de las nuevas funcionalidades de Thunderbird. Hay nuevas herramientas de búsqueda, mensajes en pestañas, archivado de mensajes y cientos de agregados para que personalices  Thunderbird 3.1. </p>

<h2>Es más fácil actualizar tus cuentas con el nuevo asistente de migración</h2>

<p>Dentro del menú Ayuda, elegí el Asistente de migración para ayudarte a cambiar la visualización de las barras de herramientas y agregar funciones como el Encabezado compacto y las Columnas avanzadas de las carpetas. </p>

<h2>Búsqueda mejorada con nuevas herramientas</h2>

<p>La nueva barra de herramientas de filtro rápido te permite filtrar tus mensajes más rápido. Filtralos a partir de «mensajes nuevos», «etiquetas», personas en tu libreta de direcciones o con términos de búsqueda. También podés elegir o guardar un filtro y usarlo a través de muchas carpetas. </p>

<p>Escribí tu término de búsqueda en la barra de búsqueda global y se abrirá una nueva pestaña para mostrarte los resultados. Podrás refinar tu búsqueda con los filtros de Thunderbird y la línea de tiempo. </p>

<h2>Mensajes en pestañas</h2>

<p>Si te gusta la navegación con pestañas de Firefox, te va a encantar el uso de pestañas con tus mensajes. Hacé docble clic o apretá la tecla Enter para abrir el mensaje en una nueva pestaña. También podés hacer clic con el botón derecho sobre mensajes o carpetas para abrirlos en una pestaña sin perder el foco en la actual.</p>

<h2>Archivado de mensajes</h2>

<p>Hacé clic en el botón Archivar o apretá la tecla «A» para archivar un mensaje. Al archivarlo, el mensaje se mueve de la Bandeja de entrada a un nuevo sistema de archivado por carpetas.</p>

<h2>Más funciones para descubrir:</h2>

<ul>
  <li>Libreta de contactos con un sólo clic</li>
  <li>Recordatorio de agregados</li>
  <li>Carpetas unificadas</li>
  <li>Administrador de actividad</li>
  <li>Cientos de agregados para probar</li>
</ul>

