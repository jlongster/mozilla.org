<h2>Wat is nij yn Thunderbird 3.1</h2>

<p>We hoopje dat jo de nije funksjes yn Thunderbird wurdearje. We hawwe nij sykark, e-mail yn ljepblêden, berjochtargivearing en hunderten tafoegings om Thunderbird 3.1 hielendal fan jo te meitsjen. </p>

<h2>Makliker om te fernijen mei de nij migraasje-assistint</h2>

<p>Under it Helpmenu, selektearje de Migraasje-assistint om jo te helpen mei it wizigjen fan de arkbalke en mooglikheden ta te foegjen lykas Ynklapte koppen en Avansearre mapkolommen. </p>

<h2>Better sykje mei nij sykark</h2>

<p>Mei de nije fluchfilterarkbalke kinne jo jo e-mail flugger filterje. Filter jo e-mail op nije berjochten, labels, persoanen yn jo adresboek, of troch it gebrûk fan syktermen. Jo kinne in filter ek &quot;fêstsette&quot; of bewarje en yn mear mappen brûke.</p>

<p>Fier jo sykterm yn de algemiene sykbalke yn en jo sykresultaten wurde yn in nij ljepblêd iepene. Mei Thunderbirds filter- en tiidslinehelpmiddelen kinne jo krekt dat e-mailberjocht fine dat jo sykje.  </p>

<h2>E-mail yn ljepblêden</h2>

<p>As jo wille hawwe fan it blêdzjen mei ljepblêden yn Firefox, dan sille jo genietsje fan e-mail yn ljepblêden. Dûbelklik of toets enter op in e-mailberjocht om berjochten te iepenjen yn in nij ljepblêd. Jo kinne rjochtsklikke op berjochten of mappen om se te iepenjen yn in eftergrûn ljepblêd.</p>

<h2>Berjochtargivearring</h2>

<p>Seleksje fan de Argivearknop of troch de ‘A’-toets sil jo e-mail argivearje. E-mail argivearje ferpleatst e-mail fan jo Postfek Yn nei it nije argyfmapsysteem.</p>

