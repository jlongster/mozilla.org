<h2>Galingesnė paieška ir naujos paieškos priemonės</h2>

<p>Nauja sparčiojo filtro juosta padės greičiau filtruoti rodomus laiškus – atrinkti tik neskaitytus, turinčius gaires, gautus iš adresų knygoje esančių adresatų ar turinčius norimą paieškos žodį. Be to, filtrą galėsite užfiksuoti – pažymėti, kad jis būtų išlaikomas, pereinant į kitą aplanką.</p>

