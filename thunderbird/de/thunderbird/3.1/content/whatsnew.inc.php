<h2>Neues in Thunderbird 3.1</h2>

<p>Wir hoffen, dass Ihnen die neuen Funktionen von Thunderbird gefallen. Es gibt neue Suchfunktionen, Nachrichten-Tabs, Nachrichten-Archivierung und hunderte Add-ons, um Thunderbird 3.1 Ihren Bedürfnissen anzupassen.</p>

<h2>Leichtere Aktualisierung mit dem neuen Migrationsassistenten</h2>

<p>Wählen Sie im Hilfe-Menü den Migrationsassistenten, der Ihnen dabei hilft, Ihre Symbolleisten-Anordnung zu ändern und Funktionen wie die kompakte Kopfzeilenansicht und Info-Spalten in der Ordnerübersicht hinzuzufügen.</p>

<h2>Bessere Suche mit neuen Suchwerkzeugen</h2>

<p>Die neue Werkzeugleiste &#8222;Schnellfilter&#8220; ermöglicht es Ihnen, Ihre Nachrichten schneller zu filtern. Filtern Sie Ihre E-Mails nach neuen Nachrichten, Schlagwörtern, Personen in Ihrem Adressbuch oder unter Verwendung von Suchbegriffen. Sie können einen Filter auch &#8222;anheften&#8220; oder speichern und ihn in mehreren Ordnern verwenden.</p>

<p>Geben Sie Ihren Suchbegriff in die globale Suchleiste ein und Sie erhalten die Suchergebnisse in einem neuen Tab. Sie können das, wonach Sie suchen, mit den neuen Filter- und Zeitleiste-Werkzeugen von Thunderbird genau lokalisieren.</p>

<h2>Nachrichten-Tabs</h2>

<p>Wenn Sie das Arbeiten mit Tabs in Firefox mögen, werden Sie auch die Nachrichten-Tabs mögen. Öffnen Sie Nachrichten per Doppelklick oder durch Drücken der Eingabetaste in einem neuen Tab. Mit einem Klick mit der mittleren Maustaste können Sie Nachrichten oder ganze Ordner im Hintergrund öffnen.</p>

<h2>Nachrichten-Archivierung</h2>

<p>Archivieren Sie Ihre Nachrichten durch Klicken der Schaltfläche &#8222;Archivieren&#8220; oder Drücken der Taste &#8222;A&#8220;. Dadurch werden die gewählten Nachrichten aus dem Posteingang in das Archiv-Ordnersystem verschoben.</p>

<h2>Weitere neue Funktionen:</h2>

<ul>
  <li>Adressbucheinträge mit einem Klick</li>
  <li>Anhang-Erinnerung</li>
  <li>Gruppierte Ordner</li>
  <li>Aktivitäten-Manager</li>
  <li>Hunderte neue Add-ons zum Ausprobieren</li>
</ul>

