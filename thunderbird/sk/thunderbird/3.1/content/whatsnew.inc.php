<h2>Čo je nové v Thunderbirde 3.1</h2>

<p>Dúfame, že nové funkcie Thunderbirdu sa vám budú páčiť. Máme nové vyhľadávacie nástroje, prehliadanie správ na kartách, archivácia správ a stovky doplnkov, ktoré vám pomôžu prispôsobiť si Thunderbird 3.1 podľa svojich predstáv.</p>

<h2>Jednoduchšia aktualizácia s novým Asistentom pri prechode na novú verziu</h2>

<p>V ponuke Pomocník nájdete položku Asistent pri prechode na novú verziu, ktorý vám môžete upraviť si vzhľad panela s nástrojmi či pridať nové funkcie ako Zobrazenie Úsporná hlavička alebo pokročilé stĺpce priečinkov.</p>

<h2>Lepšie vyhľadávanie s novými nástrojmi vyhľadávania</h2>

<p>Nový panel nástrojov Rýchly filter umožňuje filtrovať správy ešte rýchlejšie. Správy môžete filtrovať podľa toho, či sú prečítané, majú priradené značky, pochádzajú od ľudí vo vašom zozname kontaktov alebo použitím výrazov vyhľadávania. Filter môžete takisto &quot;pripnúť&quot; alebo uložiť a používať ho tým pre všetky priečinky.</p>

<p>Zadajte hľadaný výraz do poľa globálneho vyhľadávania a výsledky vyhľadávania sa otvoria na novej karte. Pomocou filtrovacích nástrojov a časovej osi rýchlo nájdete to, čo hľadáte.</p>

<h2>Prehliadanie správ na kartách</h2>

<p>Ak sa vám páči prehliadanie webu na kartách vo Firefoxe, určite sa vám bude páčiť aj naše prehliadanie správ na kartách. Kliknite dvakrát na správu alebo stlačte kláves Enter a správa sa otvorí na novej karte. Na správu alebo priečinok môžete kliknúť aj pravým tlačidlom myši a otvoriť na ju novej karte na pozadí.</p>

<h2>Archivácia správ</h2>

<p>Kliknutím na tlačidlo Archivovať alebo stlačením klávesu ‘A’ archivujete označenú správu. Archivácia znamená presun správy z priečinka Doručená pošta do priečinka Archív nášho nového archivačného systému.</p>

<h2>Ďalšie nové funkcie, ktoré stoja za povšimnutie:</h2>

<ul>
  <li>Adresár na jednom kliknutie</li>
  <li>Pripomínanie zabudnutej prílohy</li>
  <li>Zoskupené priečinky</li>
  <li>Správca aktivity</li>
  <li>Stovky nových rozšírení</li>
</ul>

