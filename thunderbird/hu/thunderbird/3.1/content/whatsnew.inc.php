<h2>Újdonságok a Thunderbird 3.1 verzióban</h2>

<p>Bízunk benne, hogy tetszenek a Thunderbird új funkciói. Új keresőeszköz, lapkezelés, archiválás és több száz kiegészítő miatt érezheti a Thunderbird 3.1 levelezőprogramot a sajátjának.</p>

<h2>Egyszerűbb frissítés az Áttérési segéd használatával</h2>

<p>A Súgó menü alatt válassza ki az Áttérési segéd menüpontot, hogy segítséget kapjon az Eszköztár módosításához és a Kompakt fejléc és a Speciális mappaoszlopok hozzáadásához. </p>

<h2>Jobb keresés az új eszközökkel</h2>

<p>Az új Gyorszűrő sáv használatával a levelek gyorsabban megtalálhatók. Szűrhet a levelek között új üzenetek, címkék, a címjegyzékben található emberek. A keresési eredményeket &quot;Kitűzheti&quot; vagy elmentheti és több mappán keresztül használhatja.</p>

<p>Írja be a keresősávba a keresett kifejezést és a keresés eredménye egy új lapon fog megjelenni. A Thunderbird szűrőeszközeivel és időrendi nézetével könnyedén megtalálhatja, amit keres. </p>

<h2>Lapok a levelezésben</h2>

<p>Ha megszerette a Firefoxban található lapkezelést, akkor ugyanezt a funkciót imádni fogja a levélkezelésben is. Kattintson kétszer a levélre vagy nyomja meg az Enter gombot és a levél egy új lapon fog megnyílni. Az üzenetekre jobb egérgombbal kattintva az új üzenetek vagy mappák a háttérben új lapon fognak megnyílni.</p>

<h2>Üzenetarchívum</h2>

<p>Az Archiválás, vagy az ‘A’ billentyűt megnyomásával archiválhatja leveleit. Archiváláskor a levelek a Beérkezett üzenetek mappából az új archív mappában kerülnek.</p>

<h2>További új funkciók:</h2>

<ul>
  <li>Egykattintásos címjegyzék</li>
  <li>Mellékletemlékeztető</li>
  <li>Egyesített mappák</li>
  <li>Tevékenységkezelő</li>
  <li>Több száz kipróbálható kiegészítő</li>
</ul>

