<h2>Zer berri Thunderbird 3.1en?</h2>

<p>Espero dugu Thunderbirden ezaugarri berriak gustoko izango dituzula. Bilaketa-tresna berriak, fitxa bidezko posta elektronikoa, mezuen artxibatzea eta Thunderbird 3.1 zeure egiteko ehunaka gehigarri.</p>

<h2>Bertsio-berritze errazagoa migraziorako morroi berriarekin</h2>

<p>Laguntza menuan, aukeratu Migraziorako morroia tresna-barraren diseinua aldatu eta Goiburuko trinkoa eta Karpeten zutabe aurreratuak bezalako ezaugarriak gehitzeko.</p>

<h2>Bilaketa hobea bilaketarako tresna berriekin</h2>

<p>Iragazki bizkorraren tresna-barra berriarekin zure posta elektronikoa azkarrago iragaziko duzu. Iragazi zure mezu elektroniko berrien, etiketen, zure helbide-liburuko pertsonen edo bilaketa-terminoen arabera. Iragaziak gorde eta hainbat karpeten artean ere erabil ditzakezu.</p>

<p>Idatzi zure bilaketa-terminoa bilaketa globalaren barran eta emaitzak fitxa berri batean irekiko ditugu. Bila zabiltzana zehatz-mehatz aurki dezakezu Thunderbirden iragazte- eta denbora-tresnekin.</p>

<h2>Fitxa bidezko posta elektronikoa</h2>

<p>Firefoxen fitxa bidezko nabigazioa gustoko baduzu, fitxa bidezko posta elektronikoa ere gustatuko zaizu. Egin klik bikoitza edo sakatu enter posta-mezu batean mezuak fitxa-leiho berrian irekitzeko. Atzeko planoko fitxan irekitzeko, eskuin-klika egin dezakezu mezu edo karpetetan.</p>

<h2>Mezuak artxibatzea</h2>

<p>Artxibatu botoia aukeratuta edo ‘A’ tekla sakatuta zure posta elektronikoa artxibatuta geratuko da. Posta artxibatzean, sarrerako ontzitik artxiboko karpeta-sistema berrira aldatuko dira lekuz zure mezuak.</p>

