<h2>Novetats del Thunderbird 3.1</h2>

<p>Esperem que gaudiu de les noves característiques del Thunderbird. Tenim noves eines de cerca, correu en pestanyes, arxivament de missatges i centenars de complements per fer-vos vostre el Thunderbird 3.1.</p>

<h2>Més fàcil d'actualitzar amb el nou auxiliar de migració</h2>

<p>Sota el menú Ajuda, seleccioneu l'auxiliar de migració per ajudar-vos a canviar el format de la barra d'eines i afegir característiques com ara la capçalera compacta o les columnes de carpeta avançades. </p>

<h2>Una millor cerca amb noves eines</h2>

<p>La nova barra de filtre ràpid us permet filtrar el vostre correu més ràpidament. Destrieu-ne els missatges nous, mostreu-ne segons l'etiqueta, o filtreu els d'aquella gent que tingueu a la llibreta d'adreces o d'acord amb uns termes de cerca. També podeu «marcar» o desar un filtre i emprar-lo en altres carpetes.</p>

<p>Escriviu el vostre terme de cerca en la barra de cerca global i s'obriran els resultats en una pestanya nova. A continuació podeu concretar què cerqueu amb les eines de filtre i de línia temporal. </p>

<h2>Correu en pestanyes</h2>

<p>Si us agrada la navegació en pestanyes del Firefox, us fascinarà el correu en pestanyes. En fer doble clic o prémer Retorn sobre un missatge de correu obrireu els missatges en una pestanya nova de la finestra. Si feu clic secundari (dret) als missatges o a les carpetes, els obrireu en una pestanya en segon terme.</p>

<h2>Arxivament de missatges</h2>

<p>En fer clic al botó Arxiva o a la tecla ‘A’ arxivareu el vostre correu. En arxivar, es mou el correu de la safata d'entrada al nou sistema de carpetes d'arxivament.</p>

<h2>Altres característiques noves per explorar:</h2>

<ul>
  <li>Llibreta d'adreces amb un sol clic</li>
  <li>Recordatori d'adjuncions</li>
  <li>Carpetes unificades</li>
  <li>Gestor d'activitats</li>
  <li>Centenars de nous complements per provar</li>
</ul>

