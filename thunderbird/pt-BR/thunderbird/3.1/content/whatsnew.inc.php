<h2>O que há de novo no Thunderbird 3.1</h2>

<p>Esperamos que você goste dos novos recursos do Thunderbird. Nós incluímos novas ferramentas de pesquisa, mensagens em abas, arquivamento de mensagens e centenas de complementos que tornam o Thunderbird 3.1 todo seu.</p>

<h2>Mais fácil de atualizar com o novo Assistente de Migração</h2>

<p>Sob o menu Ajuda, selecione o Assistente de migração para ajudá-lo a personalizar o leiaute das suas barras de ferramentas e adicionar novos recursos como o Cabeçalho compacto e Colunas avançadas das pastas.</p>

<h2>Pesquisa mais eficiente com novas ferramentas de pesquisa</h2>

<p>A nova barra de filtragem permite que você filtre suas mensagens de forma mais rápida. Comece a digitar palavras na barra de filtragem e os resultados serão exibidos instantaneamente. Você também poderá adicionar outros critérios, como mensagens não-lidas, com tags ou de pessoas nos seus catálogos de endereços. Você também poderá "fixar" ou salvar um filtro para usá-lo em várias pastas.</p>

<p>Digite um termo na barra global de pesquisas e os resultados serão abertos em uma nova aba. Você poderá identificar a mensagem que procura usando as ferramentas de filtragem e linha de tempo do Thunderbird.</p>

<h2>Abas</h2>

<p>Se você gosta da navegação por abas do Firefox, você irá adorar gerenciar suas mensagens em abas. Clique duas vezes ou pressione <kbd>Enter</kbd> em uma mensagem para abri-la em uma nova aba. Você poderá clicar com o botão de rolagem (botão do meio) em uma mensagem ou pasta para abri-las em uma nova aba e em segundo plano.</p>

<h2>Arquivamento de mensagens</h2>

<p>Ao clicar no botão Arquivar ou pressionar a tecla <kbd>A</kbd> irá arquivar suas mensagens. Arquivar significa que suas mensagens serão movidas da sua Caixa de Entrada para uma nova pasta do sistema.</p>

<h2>Novos recursos a serem explorados:</h2>

<ul>
  <li>Adicionar ao catálogo em um clique</li>
  <li>Alerta de anexos</li>
  <li>Pastas unificadas</li>
  <li>Gerenciador de atividades</li>
  <li>Centenas de complementos gratuitos</li>
</ul>

