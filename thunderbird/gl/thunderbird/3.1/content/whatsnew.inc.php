<h2>Novidades do Thunderbird 3.1</h2>

<p>Agardamos que goce cas novas características do Thunderbird. Hai novas ferramentas de busca, separadores para o correo, arquivado de mensaxes e centos de complementos para que poña o Thunderbird 3.1 ao seu xeito.</p>

<h2>Máis fácil de anovar co novo Asistente de migración</h2>

<p>Seleccione no menú Axuda o Asistente de migración para axudarlle a cambiar a disposición das barras de ferramentas e engadir características como as cabeceiras contraídas e as columnas de cartafol avanzadas. </p>

<h2>Busca mellorada con novas ferramentas de busca</h2>

<p>A nova barra de filtro rápido permitiralle filtrar máis rápido o seu correo. Comece introducindo palabras na caixa de busca de filtro rápido e mostraranse os resultados no momento. Ou tamén pode filtrar as mensaxes usando os filtros de mensaxes novas, etiquetas, persoas da súa axenda ou usando termos de busca. Tamén pode &quot;Marcar&quot; ou gardar un filtro e usalo en varios cartafoles. </p>

<p>Introduza o termo de busca na barra de Busca global e abriranse os resultados da busca nun novo separador. Pode refinar a súa busca cos filtros do Thunderbird e as ferramentas de liña de tempo. </p>

<h2>Correo en separadores</h2>

<p>Se lle gusta a navegación con separadores do Firefox vaille encantar o correo en separadores. Prema dúas veces co rato ou prema Intro nunha mensaxe para abrir as mensaxes nun novo separador. Pode premer co botón dereito nas mensaxes ou nos cartafoles para abrilos nun separador en segundo plano.</p>

<h2>Arquivado de menxases</h2>

<p>Se preme no botón Arquivar ou prema a tecla 'A' arquivarase a mensaxe. O arquivado de correo move a mensaxe desde a caixa de entrada ao novo cartafol do sistema para arquivar.</p>

<h2>Máis características novas para explorar:</h2>

<ul>
  <li>Axenda de enderezos nun só clic</li>
  <li>Recordatorio de anexos</li>
  <li>Cartafoles unificados</li>
  <li>Xestor de actividade</li>
  <li>Centos de novos complementos para probar</li>
</ul>

