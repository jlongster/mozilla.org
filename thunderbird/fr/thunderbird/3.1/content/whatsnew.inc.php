<h2>Merci d'avoir choisi Thunderbird&nbsp;3.1</h2>

<p>Nous espérons que vous aimerez les nouvelles fonctionnalités de Thunderbird. Nous avons de nouveaux outils de recherche, la messagerie avec onglets, l'archivage des messages et des centaines de modules complémentaires pour personnaliser Thunderbird&nbsp;3.1 selon vos désirs</p>

<h2>Une migration plus facile avec le nouvel assistant de migration</h2>

<p>Dans le menu d'aide, sélectionner «&nbsp;Assistant à la migration&nbsp;» pour vous aider à changer la disposition de la barre d'outils ou ajouter des fonctionnalités comme les en-têtes compacts ou les colonnes avancées de dossier. </p>

<h2>Une meilleure recherche avec de nouveaux outils</h2>

<p>La nouvelle barre d'outils de filtre rapide vous permet de filtrer plus rapidement vos messages. Filtrez vos messages par «&nbsp;Non lus&nbsp;», «&nbsp;Étiquettes&nbsp;» «&nbsp;Contacts&nbsp;» ou en utilisant des termes de recherche. Vous pouvez aussi «&nbsp;piquer&nbsp;» ou enregistrer un filtre et l'utiliser pour plusieurs dossiers.</p>

<p>Saisissez votre terme de recherche dans la barre de recherche globale pour afficher les résultats dans un nouvel onglet. Vous pouvez préciser votre recherche avec des filtres ou une frise chronologique.</p>

<h2>Messagerie avec onglets</h2>

<p>Si vous aimez la navigation par onglets de Firefox, vous allez adorer la messagerie avec onglets. Double-cliquez ou appuyez sur <kbd>Entrée</kbd> sur un message pour l'ouvrir dans un nouvel onglet. Vous pouvez faire un clic-droit sur des messages ou des dossiers pour les ouvrir en arrière-plan.</p>

<h2>Archivage des messages</h2>

<p>Sélectionnez le bouton <q>Archiver</q> ou appuyez sur la touche <kbd>A</kbd> pour archiver vos messages. Archiver un message le déplace de votre boîte de réception vers le nouveau système d'archivage.</p>

<h2>Plus de fonctionnalités à découvrir&nbsp;:</h2>

<ul>
  <li>Ajout de contacts au carnet d'adresses en un seul clic&nbsp;;</li>
  <li>Rappel des pièces jointes manquantes&nbsp;;</li>
  <li>Dossiers unifiés&nbsp;;</li>
  <li>Gestionnaire d'activité&nbsp;;</li>
  <li>Des centaines de nouveaux modules à essayer.</li>
</ul>

