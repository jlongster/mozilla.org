<h2>Co je nového v Thunderbirdu 3.1</h2>

<p>Doufáme, že se vám budou nové funkce Thunderbirdu líbit. Thunderbird obsahuje novou lištu vyhledávání, prohlížení zpráv v panelech, archivaci zpráv a stovky doplňků, kterém vám umožní si Thunderbird přizpůsobit.</p>

<h2>Snadnější aktualizace díky novému průvodci migrací</h2>

<p>V nabídce Nápověda zvolte Pomocník s migrací, který vám pomůže upravit si rozestavení lišt a přidat funkce jako kompaktní záhlaví zprávy či rozšířené sloupce u složek.</p>

<h2>Lepší vyhledávání s pomocí nových nástrojů</h2>

<p>Nová lišta rychlé filtrace vám umožní nalézt vaše e-maily rychleji. Vyfiltrujte si nové zprávy, zprávy se štítky, známé kontakty nebo zprávy obsahující konkrétní výraz. Můžete též filtr &quot;připnout&quot; či uložit a použít jej tak nad více složkami.</p>

<p>Zadejte vámi hledaný výraz v globálním vyhledávacím poli a výsledky vašeho vyhledávání se vám otevřou v novém panelu. Pomocí nástrojů na filtraci a časové osy můžete detailněji specifikovat, co hledáte.</p>

<h2>Prohlížení pošty v panelech</h2>

<p>Pokud máte rádi prohlížení v panelech ve Firefoxu, budete prohlížení zpráv v panelech milovat. Když dvojklepnete na poštovní zprávu či stisknete klávesu Enter, zobrazí se poštovní zpráva v novém panelu. Pro otevření panelu na pozadí též můžete klepnout prostředním tlačítkem myši na zprávě či složce.</p>

<h2>Archivace zpráv</h2>

<p>Klepnutím na tlačítko Archivovat či stisknutím klávesy ‘A’ provedete archivaci e-mailu. Archivace e-mailu jej přesune z doručené pošty do hierarchie složek archivu.</p>

<h2>Další nové funkce, které stojí za povšimnutí:</h2>

<ul>
  <li>Rychlý přístup ke kontaktům</li>
  <li>Upozornění na přílohy</li>
  <li>Chytré složky</li>
  <li>Správce aktivit</li>
  <li>Stovky nových doplňků na vyzkoušení</li>
</ul>

