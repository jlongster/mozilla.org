<h2>Ç’ka të Re në Thunderbird 3.1</h2>

<p>Shpresojmë t’ju pëlqejnë veçoritë e reja te Thunderbird-i. Kemi mjete të reja kërkimi, email me skeda, arkivim mesazhesh, dhe qindra shtesa për ta bërë Thunderbird 3.1 tërësisht si e doni. </p>

<h2>Më i Lehtë për T’u Përmirësuar, me Ndihmësin e Ri të Migrimeve</h2>

<p>Nën Menunë e Ndihmës, përzgjidhni Ndihmësin e Migrimeve që t’ju ndihmojë në ndryshimin e formës së Panelit tuaj dhe në shtimin e veçorive të tilla si Tituj Kompaktë dhe Shtylla të Përparuara Dosjesh. </p>

<h2>Kërkim Më i Mirë me Mjete të Reja Kërkimi</h2>

<p>Paneli i ri për Filtrim të Shpejtë ju lejon të filtroni më shpejt email-et tuaj. Filtrojini email-et tuaj sipas Mesazhesh të Rinj, Etiketash, personash në Librin tuaj të Adresave, ose duke përdorur terma kërkimi. Mundeni gjithashtu ta &quot;Fiksoni&quot; ose ta ruani një Filtër dhe ta përdorni nëpër shumë dosje. </p>

<p>Shkruani termin për kërkimin tuaj te Shtylla Globale e Kërkimeve dhe përfundimet e kërkimit do t’i hapim në një skedë të re. Me mekanizmin e Thunderbird-it për filtrime dhe mjetet për rrjedhën kohore, mund ta përimtësoni çfarë po kërkonit. </p>

<h2>Email me Skeda</h2>

<p>Nëse ju pëlqen shfletimi me skeda te Firefox-i, ka për t’ju pëlqyer edhe email-i me skeda. Dyklikoni ose shtypni tastin Enter mbi një mesazh poste që ta hapni mesazhin në një skedë të re. Mund të djathtasklikoni mbi mesazhe ose dosje që t’i hapni ata në një skedë në sfond.</p>

<h2>Arkivim Mesazhesh</h2>

<p>Klikoni mbi butonin Arkivë ose shtypni tastin ‘A’ që të arkivohet email-i juaj. Arkivimi i postës i kalon email-et tuaj prej kutisë Të marrë te sistemi i ri i dosjeve të arkivës.</p>

